public class Inheritance {
    //Inheritance in java
    // Inheritance is a process in java using which one class acquires the property of another class in java and object-oriented programming
    // Class whose properties are getting acquired is known as a super class/base class/parent class and class who is acquiring the properties is called a child class/sub-class

    //Point to remember about inheritance
    //You can inherit parent class by using extends keywords in java

    // On inheritance all the methods and variable (which are not declared as private) from the parent class are inherited by the child class in java.

    //You can call methods from the parent class using child class object. Using the dot(.) operator

    //Syntax:

    //class <child-class> extends <parent-class>{
    //}


}
