public class BoundedForLoop{
        public static void main(String[] args) {
                //For Loop in Java

                //Syntax
                //for (initialization; condition; increment/decrement(update variable))
                //         {
                //         Code to execute
                //      }

//                write a java program that print int 1-10

                for (int x=1;x<=10;x++){
                        System.out.println(x);
                }


                //write a java program that print int 20 15 10 5
                for (int x=20;x>0;x-=5){
                        System.out.println(x);
                }
        }
}
